# 기본 if문
if 2 > 1:
  print("Hello")
  
if 2 < 1:
  print("Hello")
  
if not 2 < 1:
  print("bye")

if 1 > 0 and 2 > 1:
  print("and true")
  
if 1 > 0 and 2 < 1:
  print("and false")

if 1 > 0 or 2 < 1:
  print("or true")
  
# if elif else문
x = 3
if x > 5:
  print("hello")
elif x == 3:
  print('Bye')
else:
  print("Hi")